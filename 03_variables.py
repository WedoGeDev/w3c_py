# PYTHON crea variables al momento de asignarlos
x = 5
y = 'Omar garcia'

print(x)
print(y)

z = 4 # x is of type int
z = 'Omar garcia' # x is now of type str
print(z)

# Casting
x = str(3) # será '3'
print(x)
y = int(3) # será 3
print(y)
z = float(3) # será 3.0
print(z)

print(type(x))
print(type(y))
print(type(z))

nombre = "Omar"
print(nombre)
# es lo mismo que
nombre = 'Omar'
print(nombre)

#case sensitive
variable = 4
